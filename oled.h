/*************************写一个八位命令 从高向低写****************************/
void WriteCommand(unsigned char Data)
{
    unsigned char i;
    SetCs(0);
    SetSclk(0);
    SetMosi(0);
    udelay(time);
    SetSclk(1);
    udelay(time);
    for (i =0;i <8;i ++)
    {
       SetSclk(0);
       if ((Data&0x80) >>7) { SetMosi(1); }
       else 
       { SetMosi(0); }
       Data =Data <<1;
       udelay(time);
       SetSclk(1);
       udelay(time);
    }
    SetCs(1); 
}

/*************************写一个八位数据 从高向低写****************************/
void WriteData(unsigned char Data)
{
    unsigned char i;
    SetCs(0);
    SetSclk(0);
    SetMosi(1);
    udelay(time);
    SetSclk(1);
    udelay(time);
    for (i=0; i<8; i++)
    {
       SetSclk(0);
       if ((Data&0x80) >>7) { SetMosi(1); }
       else 
       { SetMosi(0); }
       Data = Data <<1;
       udelay(time);
       SetSclk(1);
       udelay(time);
    }
    SetCs(1);
}
