/************************延时函数*********************************************/
void udelay(uint16_t t)                                                         //400   10us
{
  uint16_t i;
  for(i =0;i <t;i ++) { asm("nop");}
}
void mdelay(uint16_t t)                                                         //10     10ms
{
  uint16_t i, n;
  for(i =0;i <18000;i ++)
  {
    for(n =0;n <t;n ++) { asm("nop"); }
  }
}

