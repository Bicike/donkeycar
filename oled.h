void LCD_Draw_circle(unsigned char xc, unsigned char yc, unsigned char x, unsigned char y)
{
  LCD_PutPixel( xc + x, yc + y);    
  LCD_PutPixel( xc - x, yc + y);    
  LCD_PutPixel( xc + x, yc - y);   
  LCD_PutPixel( xc - x, yc - y);    
  LCD_PutPixel( xc + y, yc + x);   
  LCD_PutPixel( xc - y, yc + x);    
  LCD_PutPixel( xc + y, yc - x);    
  LCD_PutPixel( xc - y, yc - x);  
}
//只计算八分圆周上的点，其余圆周上的点利用对称性就可得到。
//只需要知道了圆上的一个点的坐标 (x, y) ，利用八对称性，我们马上就能得到另外七个对称点的坐标。
void LCD_Drawcircle(unsigned char xc, unsigned char yc, unsigned char r, unsigned char fill)
{
    int x =0,y=r,yi,d;
    d =3-2 *r;//计算决策参数初始值
        
    if(xc +r <0 || xc -r >=240 || yc +r <0 || yc -r >=320)
    {
        return;//如果圆在可视范围之外，直接退出
    }
    if(fill)//如果填充，画实心圆
    {
        LCD_Set_Pos(xc,yc>>3);
        while(x <=y)//得到当前点以及对应的7个对称点的坐标，根据圆心(xc,yc)做相应的偏移，绘出对应点
        {
            for(yi =x;yi <=y;yi++)
            {
                LCD_Draw_circle(xc, yc, x, yi);
            }
            if(d <0)
            {
                d =d +4*x +6;
            } 
            else
            {
                d =d +4*(x -y)+10;
                y --;
            }
            x ++;
        }
    } 
    else
    {
        LCD_Set_Pos(xc,yc>>3);
        while(x <=y)
        {
            LCD_Draw_circle(xc, yc, x, y);
            if(d <0)
            {
                d =d +4*x +6;
            } 
            else
            {
                d =d +4*(x -y)+10;
                y --;
            }
            x ++;
        }
    }
}
    
